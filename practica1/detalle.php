<h2> Introducción</h2>
<p>La idea de nuestra plataforma consiste en concentrar todo lo relacionado con el alquiler SEGURO del vehículo en una sola web. <br>
Estaría principalmente enfocada en el alquiler de vehículos de terceros a clientes con
nosotros como intermediarios y vehículos de nuestra propiedad añadiendo la opción de
compra, también conocido como “renting”.<br>
Todo estas posibles opciones estarían acompañadas de magníficos packs para el cliente
asegurando su comodidad en todo momento y en el que se garantiza la fiabilidad de la
operación, mantenimiento por diferentes talleres, revisión de la ITV, seguros, parking… <br>
</p>

<h3> Funcionalidades para nuestros clientes:</h3>
<ul>
	<li><b>Alquilar su propio coche:</b>
		<p> El usuario de nuestra plataforma podrá dejarnos su vehículo para su futuro alquier a otros usuarios de la web. No se tendrá que preocupar por nada, ya que nuestro de equipo de encargá de todo el mantenimineto que este requiera durante su alquier.
		</p>
	</li>
	<li><b>Alquilar un coche:</b>
		<p>Cualquier cliente tendrá a disposición una serie de coche para su alquier con todo tipo</p>
	</li>
	<li><b>Renting:</b>
		<p>Esta opción  viene siendo muy demandada ultimamente. Es muy practica ya que consiste en el alquier de un coche a elección (dentro de un amplio conjunto de coches de diferentes gamas) por un determinado tiempo fijo. Al finalizar este tiempo de alquier, el cliente tendrá la opción de seguir alquilando el mismo coche por mas tiempo, alquilar otro vehículo por un tiempo definido o poder ADQUIRIR el coche que ha estado alquilando en el último tiempo.</p>
	</li>
</ul>
<br>
<h2> Usuarios y roles</h2>
<ul>
	<li><b>Cliente</b>
		<p> Para poder realizar cualquier operación de alquiler o renting, cualquier visitante de la web tendrá que estar logeado. Todo visitante podrá acceder a la paltaforma web y navegar por ella sin ningún problema como anónimo, pero deberá estar registrado para poder realizar cualquier operación.
		</p>
	</li>
	<li><b>Administrador</b>
		<p> Será el encargado del mantenimiento de la plataforma web. Estará en todo momento en contacto con los directivos de la empresa para cualquier posible modificación en la misma.
		</p>
	</li>
	<li><b>Comercial</b>
		<p> El comercial es el principal motor de este modelo de negocio. Será el encargado de estar en contacto con el cliente en todo momento, ya sea para responder cualquier duda del cliente o para la venta de uno de los servicios ofertados por nuestra plataforma.
		</p>
	</li>
</ul>
